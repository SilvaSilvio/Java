package AULA02;

public class exemploBreak {

	public static void main(String[] args) {

		for (int i = 0; i < 10; i++) {
			System.out.println(i);
			if (i == 6)
				break;
		}
		System.out.println("----------");
	for (int i = 0; i < 10; i++) {
		if (i == 6)
			break;
		System.out.println(i);
		
	}

}
}
